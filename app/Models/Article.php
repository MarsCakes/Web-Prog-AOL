<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;

class Article extends Model
{
  use HasFactory;

  protected $fillable = [
    'title',
    'slug',
    'excerpt',
    'body',
    'image',
    'tag',
    'read_time',
    'published_at'
  ];

  protected $casts = [
    'published_at' => 'datetime',
  ];

  public function getRouteKeyName()
  {
    return 'slug';
  }
}
